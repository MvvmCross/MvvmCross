namespace MvvmCross.iOS.Support.SidePanels
{
    /// <summary>
    /// Denotes the panel hint type
    /// </summary>
    public enum MvxPanelHintType
    {
        /// <summary>
        /// The reset root
        /// </summary>
        ResetRoot,

        /// <summary>
        /// The pop to root
        /// </summary>
        PopToRoot,

        /// <summary>
        /// The active panel
        /// </summary>
        ActivePanel,


		/// <summary>
		/// The view to be pushed
		/// </summary>
		PushView
    }
}